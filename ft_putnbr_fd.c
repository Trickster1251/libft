#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long	nbr;

	if (n < 0)
	{
		ft_putchar_fd('-', 1);
		nbr = n * -1;
	}
	else
		nbr = n;
	if (nbr >= 10)
		ft_putnbr_fd(nbr / 10, fd);
	ft_putchar_fd(nbr % 10 + 48, fd);
}
